#*Find Rad Value from Ang

π = float(3.14159265358979323846264338327950288419716939937510)

ang = float(input("Enter Angle Please- "))

rad = float(ang * π / 180)

#*Prints Formula
print('Formula - Angle * π / 180 = ')

#*Prints rad Value
print('Value of Angle in Radian - ')
print(rad)

